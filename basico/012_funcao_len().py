
"""

len() == Ver a quantidade de caracteres de uma String

"""

user = input('Digite o nome do seu usuário ')
password = input('Digite sua senha ')

qtde_caracteres_u = len(user)
qtde_caracteres_p = len(password)


print(f'O usuário digitado é {user} que possui {qtde_caracteres_u} caracteres')

print(f'A quantidade de caracteres do seu usuário e senha somados é de {qtde_caracteres_p + qtde_caracteres_u}')
