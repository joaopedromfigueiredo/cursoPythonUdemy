"""

Operadores Aritméticos

+ é Adição
- é Subtração
* é Multiplicação
** é potenciação
/ é Divisão
// é parte inteira da divisão
% é resto da divisão
== é igualdade

"""

print('Adição 5 + 5 =', 5 + 5)
print('Subtração 5 - 5 =', 5 - 5)
print('Multiplicação 5 * 5 =', 5 * 5)
print('Potenciação 5 ** 5 =', 5 ** 5)
print('Divisão 5 / 5 =', 5 / 5)
print('Parte Inteira da Divisão de 20 / 3 =', 20 / 3)
print('Resto da Divisão de 20 / 3 =', 20 % 3)
print('Multiplicação de String 3 * ab- =', 3 * 'ab-')
